function sum(a,b){
    return a+b;            // O(1) — Constant Complexity
}

function multiply(a,b){
    return a*b;          // O(1) — Constant Complexity
}

function subtract(a,b){
    return a-b;          // O(1) — Constant Complexity
}

function divide(a,b){
    return b/a;         // O(1) — Constant Complexity
}

let result=sum(3,1)
console.log(result)

result=multiply(3,1)
console.log(result)

result=subtract(3,1)
console.log(result)

result=divide(3,1)
console.log(result)